// Get all elements with the 'shown' class
var show = document.querySelectorAll('#shown');
// Get all elements with the 'hidden' class
var hidden = document.querySelectorAll('#hidden');

// Initially hide the first 'hidden' element
hidden[0].style.display = 'none';
// Add mouseover event listener to the first 'show' element
show[0].addEventListener('mouseover', () => {
    // When the mouse is over, display the 'hidden' element and hide the 'show' element
    hidden[0].style.display = 'block';
    show[0].style.display = 'none';
})
// Add mouseleave event listener to the first 'hidden' element
hidden[0].addEventListener('mouseleave', () => {
    // When the mouse leaves, display the 'show' element and hide the 'hidden' element
    show[0].style.display = 'block';
    hidden[0].style.display = 'none';
})
